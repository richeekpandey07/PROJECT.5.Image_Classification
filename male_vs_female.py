probability = model.predict_proba([resized])[0]

if prediction == 0:
    st.success("👨 Male")
else:
    st.success("👩 Female")

st.write(f"👨 Male Probability: {probability[0] * 100:.2f}%")
st.write(f"👩 Female Probability: {probability[1] * 100:.2f}%")
if uploaded_file is not None:

    image = Image.open(uploaded_file).convert("RGB")

    col1, col2 = st.columns(2)

    with col1:
        st.image(image, caption="Uploaded Image", use_container_width=True)

    # Preprocessing
    resized = image.resize((IMG_SIZE, IMG_SIZE))
    resized = np.array(resized)

    # Normalize (remove if not used during training)
    resized = resized / 255.0

    resized = resized.flatten()

    try:
        prediction = model.predict([resized])[0]

        if hasattr(model, "predict_proba"):
            probability = model.predict_proba([resized])[0]
        else:
            probability = None

        with col2:

            st.subheader("Prediction")

            if prediction == 0:
                st.success("👨 Male")
            else:
                st.success("👩 Female")

            if probability is not None:

                st.subheader("Confidence")

                st.write(
                    f"👨 Male: **{probability[0]*100:.2f}%**"
                )

                st.progress(float(probability[0]))

                st.write(
                    f"👩 Female: **{probability[1]*100:.2f}%**"
                )

                st.progress(float(probability[1]))

    except Exception as e:
        st.error(f"Prediction Error: {e}")

# ----------------------------------
# Footer
# ----------------------------------
st.markdown("---")

