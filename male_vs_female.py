import streamlit as st
import numpy as np
from PIL import Image
import joblib

# ----------------------------------
# Page Configuration
# ----------------------------------
st.set_page_config(
    page_title="Male vs Female Classifier",
    page_icon="👨👩",
    layout="centered"
)

# ----------------------------------
# Load Model
# ----------------------------------
@st.cache_resource
def load_model():
    return joblib.load("male_female_model.pkl")

try:
    model = load_model()
except Exception as e:
    st.error(f"Error loading model: {e}")
    st.stop()

IMG_SIZE = 64

# ----------------------------------
# Header
# ----------------------------------
st.markdown(
    """
    <h1 style='text-align:center;color:#4F46E5;'>
    👨 Male vs Female Image Classifier 👩
    </h1>
    """,
    unsafe_allow_html=True
)

st.markdown(
    """
    Upload an image and the model will predict whether
    the face belongs to a Male or Female.
    """
)

st.divider()

# ----------------------------------
# Upload Image
# ----------------------------------
uploaded_file = st.file_uploader(
    "📤 Upload Image",
    type=["jpg", "jpeg", "png"]
)

# ----------------------------------
# Prediction
# ----------------------------------
